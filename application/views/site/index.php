    <h2>关于本系统</h2>
    <p>随着科学技术的不断提高，计算机科学日渐成熟，其强大的功能已为人们深刻认识，它已进入人类社会的各个领域并发挥着越来越重要的作用。作为计算机应用的一部分，使用计算机对学生成绩信息进行管理，具有手工管理所无法比拟的优点。例如，、查找方便、可靠性高、存储量大、保密性好、寿命长、成本低等。这些优点能够极大的提高学生档案管理的效率，也是企业的科学化、正规化管理，与世界接轨的重要条件。因此，开发学生成绩管理系统很有必要。本系统在大多数成绩管理系统的基础上，主要增加了教师对成绩的操作，教师改完试卷后不用在往学院的教务处办公室报送成绩，可以直接的把成绩上传到网络上，学生也可以方便快速的查询到自己的成绩，考试后教务管理人员也不必总呆在学院的办公室，他们都不受时间，位置，空间的限制，只要有上网的条件，在家里就可以完成有关成绩的录入，更新，管理，查询和删除。本系统将会改变以前靠手工管理学生成绩的状况，提高工作效率。希望能为老师和学校的工作带来便利。</p>
